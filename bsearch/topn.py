from typing import List
import random


def topn(data: List[int], n: int) -> int:
    def fit_into_tops(tops: List[int], candidate: int) -> List[int]:
        return (
            [t for t in tops if t > candidate]
            + [candidate]
            + [t for t in tops if t < candidate]
            if candidate not in tops
            else tops
        )[:n]

    tops: List = [data[0]]

    for item in data:
        tops = fit_into_tops(tops, item)

    return tops


